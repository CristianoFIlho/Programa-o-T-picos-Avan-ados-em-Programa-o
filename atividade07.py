# Entrada de dados
sentenca = input('Digite a sentença: ')
janela = input('Digite a janela: ')

# Processamento
for i in range(len(sentenca) - len(janela) + 1):
    if sentenca[i:i+len(janela)] == janela:
        print('Padrão encontrado na posição', i)

# Saída de dados
print('Fim do programa')



